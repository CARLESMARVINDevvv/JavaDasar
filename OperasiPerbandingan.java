// contoh operasi perbandingan dalam Java
// Operasi perbandingan dalam Java digunakan untuk membandingkan dua nilai.
// Hasil dari operasi perbandingan ini adalah boolean (true atau false).
// Berikut adalah contoh operasi perbandingan dalam Java:
// > = lebih dari,
// < = kurang dari,
// >= = lebih dari atau sama dengan,
// <= = kurang dari atau sama dengan,
// == = sama dengan,
// != = tidak sama dengan.
// Operasi perbandingan ini sering digunakan dalam kondisi if, while, atau for untuk mengontrol alur program.


public class OperasiPerbandingan {
    public static void main(String[] args) {
        int Value1 = 100;
        int Value2 = 50;

        System.out.println(Value1 <  Value2); // false
        System.out.println(Value1 >  Value2); // true
        System.out.println(Value1 <= Value2); // false
        System.out.println(Value1 >= Value2); // true
        System.out.println(Value1 == Value2); // false

    }
}
